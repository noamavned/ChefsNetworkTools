    def __get_letter__(self, char):
        if char == " ":
            char = "SPACE"
        if char == "<":
            char = "LEFT"
        if char == ">":
            char = "RIGHT"
        if char == "*":
            char = "MID"
        return getattr(self, char.upper(), ["     "] * 6)
    
    def __generate_letter_grid__(self, s):
        letter_grids = [self.__get_letter__(char) for char in s]
        return letter_grids
    
    def __combine_rows__(self, letter_grids):
        combined_rows = [""] * 6
        for letter in letter_grids:
            for i in range(6):
                combined_rows[i] += letter[i] + " "
        return combined_rows
    
    def create_ascii_balloon(self, s):
        letter_grids = self.__generate_letter_grid__(s)
        combined_rows = self.__combine_rows__(letter_grids)
        return "\n".join(combined_rows)